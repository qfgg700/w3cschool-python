
print("** ".join(["spam", "eggs", "ham"]))
#打印 "spam, eggs, ham" 以,逗号作为分隔符拼接；
print("Hello ME".replace("ME", "world"))
#打印 "Hello world" 替换；
print("This is a sentence.".startswith("This"))
# 打印 "True" 判断是否以This为开头
print("This is a sentence.".endswith("ce."))
# 打印 "True" 判断是否以sentence.为结尾
print("This is a sentence.".upper())
# 打印 "THIS IS A SENTENCE." 所有字符串内的字母变成大写；
print("AN ALL CAPS SENTENCE".lower())
#打印  "an all caps sentence" 所有字符串内的字母变成小写；
print("spam, eggs, ham".split(","))
#打印  "['spam', 'eggs', 'ham']" 把字符串连接，形成列表；